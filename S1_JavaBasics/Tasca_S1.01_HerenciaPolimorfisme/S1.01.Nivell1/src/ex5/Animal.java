package ex5;

public class Animal {

	public String correr() {
		return "L'animal corre!";

	}

	public String correr(String nom) {
		return "L'animal " + nom + " corre moltíssim";

	}

	public String correr(String nom, int edad) {
		return "L'animal " + nom + " corre moltíssim, fins que té una edat de  " + edad + " anys";

	}

}
